import UIKit

extension UIColor {
    
    @nonobjc class var softBlue: UIColor {
        return UIColor(red: 104.0 / 255.0, green: 117.0 / 255.0, blue: 221.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var paleGrey: UIColor {
        return UIColor(red: 245.0 / 255.0, green: 246.0 / 255.0, blue: 250.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownishGrey: UIColor {
        return UIColor(white: 112.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownishGreyTransparent: UIColor {
        return UIColor(white: 112.0 / 255.0, alpha: 0.3)
    }
    
    @nonobjc class var grapefruit: UIColor {
      return UIColor(red: 235.0 / 255.0, green: 107.0 / 255.0, blue: 107.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPink: UIColor {
      return UIColor(white: 191.0 / 255.0, alpha: 1.0)
    }

}
